package com.example.lukas.pdt.home;

public class SearchFilter {

	private Integer radiusDistance;
	private Boolean bar;
	private Boolean pub;
	
	public Integer getRadiusDistance() {
		return radiusDistance;
	}

	public void setRadiusDistance(Integer radiusDistance) {
		this.radiusDistance = radiusDistance;
	}

	public Boolean getBar() {
		return bar;
	}

	public void setBar(Boolean bar) {
		this.bar = bar;
	}

	public Boolean getPub() {
		return pub;
	}

	public void setPub(Boolean pub) {
		this.pub = pub;
	}
}
